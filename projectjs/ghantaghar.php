    <h2> Ghanta Ghar: Identity of Birgunj</h2>
    <img src="Nepali Culture.jpeg" alt="Beautiful view of Nepal" class="center" /> 
    <h3>About</h3>
    <p>
    Ghanta Ghar, situated in the middle of Birgunj, has turned into a structure that identifies the city. 
    The tower was established four decades ago with the help of Japanese school kids from Yogano, Japan, who raised money for the construction and sent a Japanese branded Seiko clock-face for the tower.
    The Ghanta Ghar was built in 1976 with an active participation from the then zonal commissioner Lakshya Bahadur Gurung and the locals. It was the time of the Panchayat System and the Nagar Panchayat, where board members of the Nagar Panchayat made a decision to give the space, where Ghantaghar currently situated, to a businessman for the development of a petrol pump. Few of the board members of the Nagar Panchayat and the then deputy mayor Gopal Prasad Adhikari were against the decision. 
    They put up a fence around the free space at night and placed a plank denoting it as ‘Mahendra Park’. “Since the space had been dubbed with the name of the King’s father, the businessman did not have the courage to use the land,” the then Deputy Mayor’s, 70-year-old wife Anima Adhikari recalled,” Ghantaghar was established three years after this.”
